# ЭТАП 2: БАЗОВАЯ ОЧИСТКА
# ═════════════════════════════════════════════════════════════════════

def clean_text(text: str) -> str:
    """
    Очищает текст от HTML, ссылок, email, специальных символов
    
    Порядок важен:
    1. Привести к нижнему регистру
    2. Удалить ссылки/HTML
    3. Удалить спец.символы (оставить только буквы, цифры, пробелы)
    4. Нормализовать пробелы
    """
    if not isinstance(text, str):
        return ""

    text = text.lower()

    # Удалить ссылки / html / email
    text = re.sub(r"http\S+|www\.\S+", " ", text)
    text = re.sub(r"<.*?>", " ", text)
    text = re.sub(r"\S+@\S+", " ", text)

    # Оставить только буквы и цифры
    text = re.sub(r"[^a-zа-яё0-9\s_]", " ", text)

    # Нормализация пробелов
    text = re.sub(r"\s+", " ", text)

    return text.strip()


