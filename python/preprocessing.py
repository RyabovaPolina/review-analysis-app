def preprocess_text(text, use_negations=True, use_domain_slang=True, normalize_repeated=True):
    """
    Полный препроцессинг (версия v4) с правильным порядком обработки:
    
    1. Нормализация дублей гласных (ооочень → очень)
    2. Очистка текста (удаление HTML, спец.символов)
    3. Применение доменных фраз (кот наплакал → количество_мало)
    4. Токенизация
    5. Применение доменного сленга (пушка → товар_очень_хороший)
    6. Обработка отрицаний (не товар_очень_хороший → не_товар_очень_хороший)
    
    Args:
        text: исходный текст
        use_negations: использовать ли обработку отрицаний
        use_domain_slang: использовать ли доменный словарь
        normalize_repeated: использовать ли нормализацию дублей
    
    Returns:
        очищенный и обработанный текст
    
    Примеры:
    "Ооочень класная пушка!" 
    → "очень класная товар_очень_хороший"
    
    "Не очень, кот наплакал товара"
    → "не_очень количество_мало товара"
    
    "Шляпа! Век жди!" 
    → "товар_плохой доставка_очень_медленная"
    """
    # ШАГ 1: Нормализация дублей (если включено)
    if normalize_repeated:
        text = normalize_repeated_chars(text)
    
    # ШАГ 2: Очистка текста
    text_clean = clean_text(text)

    if not text_clean:
        return ""

    # ШАГ 3: Применяем доменные фразы (многословные)
    if use_domain_slang:
        text_clean = apply_domain_phrases(text_clean)

    # ШАГ 4: Токенизируем
    tokens = tokenize(text_clean)

    # ШАГ 5: Применяем доменный сленг (однословный)
    if use_domain_slang:
        tokens = apply_domain_slang(tokens)

    # ШАГ 6: Обрабатываем отрицания
    if use_negations:
        tokens = combine_negations(tokens)

    # Fallback: если всё удалилось
    if not tokens:
        return text_clean

    return " ".join(tokens)


