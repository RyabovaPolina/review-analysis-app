def normalize_repeated_chars(text: str) -> str:
    """
    Нормализует дублированные гласные и согласные (ПЕРЕД приведением к нижнему регистру)
    
    ооочень → очень
    ООООЧЕНЬ → очень  
    ужассссно → ужасно
    кккррассиво → красиво
    
    Правило: 3+ повторения одного символа → 1 символ
    (2 повторения оставляем, т.к. некоторые слова имеют 2 буквы: нн, сс)
    """
    if not isinstance(text, str):
        return ""
    
    # Преобразуем в нижний регистр ДО нормализации дублей
    text = text.lower()
    
    # Гласные: а е ё и о у ы э
    # \1{2,} означает "2 или более повторений" → заменяем на 1
    text = re.sub(r'([аеёиоуыэ])\1{2,}', r'\1', text)
    
    # Согласные: только самые часто встречающиеся в русском
    # Заменяем 3+ повторения на 1 повторение
    text = re.sub(r'([кпрсжждтбфхцчщнмлйвгз])\1{2,}', r'\1', text)
    
    return text


